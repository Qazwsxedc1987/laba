<?php

// Функция для расчета скорости, времени или пути
function calculate($speed = null, $time = null, $distance = null)
{
    // Проверяем, какие параметры переданы, и выполняем соответствующие расчеты
    if ($time !== null && $distance !== null) {
        // Определяем скорость
        $speed = $distance / $time;
        return "Определим скорость, если время=$time, а путь=$distance. Скорость=$speed";
    } elseif ($speed !== null && $distance !== null) {
        // Определяем время
        $time = $distance / $speed;
        return "Определим время, если скорость=$speed, а путь=$distance. Время=$time";
    } elseif ($speed !== null && $time !== null) {
        // Определяем путь
        $distance = $speed * $time;
        return "Определим путь, если время=$time, а скорость=$speed. Путь=$distance";
    } else {
        // Если не передано ни одного параметра, возвращаем сообщение об ошибке
        return "Необходимо передать хотя бы два параметра для расчета.";
    }
}

// Распечатываем результаты для всех вариантов
echo calculate(10, 15) . "<br>"; // Определим скорость, если время=10, а путь=15. Скорость=1.5
echo calculate(15, null, 150) . "<br>"; // Определим время, если скорость=15, а путь=150. Время=10
echo calculate(null, 10, 150) . "<br>"; // Определим путь, если время=10, а скорость=15. Путь=150